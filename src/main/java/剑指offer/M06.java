public class M06 {

    public class ListNode {
        int val;
        ListNode next;

        ListNode(int x) {
            val = x;
        }
    }

    public int[] reversePrint(ListNode head) {

        int[] result = new int[10001];
        int i = 10000;
        while (head != null) {
            result[i] = head.val;
            i--;
            head = head.next;
        }

        result = Arrays.copyOfRange(result, i + 1, result.length - 1);

        return result;
    }


}
